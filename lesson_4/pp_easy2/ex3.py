# Makes code modular and reusable, DRY principal 
# Encapsulation 'hides' implementation details and instead focuses on interaction
# of objects through interfaces allowing internal changes to not affect users.
# Polymorphism allows for methods that may behave similarly and share name, but have different 
# implementations to be overriden for consistency. Inheritance allows similar objects to share
# Behaviors without having to rewrite entirely different classes. Abstraction allows 
# code bases to manage complexity.